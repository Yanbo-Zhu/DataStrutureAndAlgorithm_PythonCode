def insert_sort(array):
    n = len(array)
    for i in range(1, n):
        if array[i] < array[i - 1]:
            temp = array[i]
            j = i
            while j > 0 and array[j - 1] > temp:
                array[j] = array[j - 1]
                j -= 1
            array[j] = temp

# Example usage:
arr = [64, 25, 12, 22, 11]
insert_sort(arr)
print("Sorted array is:", arr)